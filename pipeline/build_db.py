def labels_for(spec):
    if spec[0] == "range":
        return [str(n) for n in range(spec[1], spec[2] + 1)]
    return list(spec[1])

def main():
    if os.path.exists(DB): os.remove(DB)
    con = sqlite3.connect(DB)
    con.executescript(open("schema.sql").read())
    cur = con.cursor()
    for pid, p in PARKS.items():
        cur.execute("INSERT INTO parks(id,name,region,url,blurb,fishing,fmz,sort,backcountry,facilities) VALUES(?,?,?,?,?,?,?,?,?,?)",
            (pid, p["name"], p["region"], p["url"], p["blurb"], p["fishing"], p["fmz"], p["sort"], 1 if p.get("backcountry") else 0, json.dumps(p["facilities"])))
    total = 0
    for (pid, name, sub, sort, spec) in CAMPGROUNDS:
        cur.execute("INSERT INTO campgrounds(park_id,name,sub,sort) VALUES(?,?,?,?)", (pid, name, sub, sort))
        cg_id = cur.lastrowid
        rows = [(cg_id, lbl, 0, SRC) for lbl in labels_for(spec)]
        cur.executemany("INSERT INTO sites(campground_id,label,verified,source) VALUES(?,?,?,?)", rows)
        total += len(rows)
    for (pid, tname, length, diff, detail, tsort) in TRAILS:
        cur.execute("INSERT INTO trails(park_id,name,length_km,difficulty,detail,sort) VALUES(?,?,?,?,?,?)",
                    (pid, tname, length, diff, detail, tsort))
    con.commit()
    print(f"Built {DB}: {len(PARKS)} parks, {len(CAMPGROUNDS)} campgrounds, {total} sites, {len(TRAILS)} trails\n")
    for pid, name, sort in cur.execute("SELECT id,name,sort FROM parks ORDER BY sort").fetchall():
        n = con.execute("SELECT COUNT(*) FROM sites s JOIN campgrounds c ON s.campground_id=c.id WHERE c.park_id=?",(pid,)).fetchone()[0]
        print(f"  {sort:>3}  {name:16} {n:>5} sites")
    con.close()

if __name__ == "__main__":
    main()
